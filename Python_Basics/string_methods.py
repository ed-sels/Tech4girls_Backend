# python3 
# upper()
mv_string = "Hello,world"
print("Upper case:",mv_string.upper())

# lower()
mv_string = "HELLO,WORLD"
print("Lowercase:",mv_string.lower())

# replace()
mv_string = "Hello,world,you are beautiful"
new_word = mv_string.replace("beautiful","big")
print("Replaced:",new_word)

# join()
mv_string = ["hello", "world"]
print("Join:","".join(mv_string))
